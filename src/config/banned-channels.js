/**
 * Normaliza el nombre de un canal para comparación
 * @param {string} channelName - Nombre del canal
 * @returns {string} - Nombre normalizado
 */
function normalizeChannelName(channelName) {
  if (!channelName || typeof channelName !== 'string') {
    return '';
  }
  
  return channelName
    .trim()
    .toLowerCase()
    .replace(/[^a-z0-9\s]/g, '') // Remover caracteres especiales
    .replace(/\s+/g, ' ') // Normalizar espacios
    .trim();
}

/**
 * Verifica si un canal está en la lista de prohibidos
 * @param {string} channelName - Nombre del canal a verificar
 * @returns {boolean} - true si el canal está prohibido
 */
function isChannelBanned(channelName) {
  if (!channelName) {
    return false;
  }
  
  const normalizedInput = normalizeChannelName(channelName);
  
  return BANNED_CHANNELS.some(bannedChannel => {
    const normalizedBanned = normalizeChannelName(bannedChannel);
    return normalizedInput === normalizedBanned;
  });
}

